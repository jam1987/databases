SELECT t1.CODBEB
FROM GUSTA t1
WHERE EXISTS (SELECT *
	      FROM GUSTA t2, BEBIDA t3
	      WHERE t3.NOMBREBEB='Malta' AND
	      t2.CODBEB=t3.CODBEB AND
	      t2.CI=t1.CI);

-- PREGUNTA 19

SELECT t1.CODFS
FROM FRECUENTA t1
WHERE EXISTS (SELECT *
	      FROM GUSTA t2, BEBIDA t3
	      WHERE t3.NOMBREBEB='Malta' AND
	      t3.CODBEB=t2.CODBEB AND t1.CI=t2.CI);

