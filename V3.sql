SELECT t1.CODFS
FROM FUENTESSODA t1
WHERE NOT EXISTS (SELECT *
		  FROM VENDE t2
		  WHERE t1.CODFS=t2.CODFS AND
		  NOT EXISTS (SELECT *
			      FROM FRECUENTA t3, BEBEDOR t4
			      WHERE t3.CI=t4.CI AND
			      t4.NOMBRE='Luis Perez' AND
			      NOT EXISTS (SELECT *
					  FROM VENDE t6
					  WHERE t6.CODFS = t3.CODFS AND
					  t6.CODBEB=t2.CODBEB)));


